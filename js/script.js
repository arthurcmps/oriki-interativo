document.getElementById('gerar').addEventListener('click', () => {
    const tema = document.getElementById('tema').value;
    const tom = document.getElementById('tom').value;

    if (orikis[tema] && orikis[tema][tom]) {
        const versos = orikis[tema][tom];
        const versoEscolhido = versos[Math.floor(Math.random() * versos.length)];
        document.getElementById('oriki').textContent = versoEscolhido;
    } else {
        document.getElementById('oriki').textContent = "Oríkì ainda não disponível para esse orixá ou tom.";
    }
});

// Função para alternar modo escuro
const toggleBtn = document.getElementById('toggleDarkMode');

function setDarkMode(enabled) {
    if (enabled) {
        document.body.classList.add('dark-mode');
        localStorage.setItem('darkMode', 'enabled');
        toggleBtn.textContent = '☀️ Modo Claro';
    } else {
        document.body.classList.remove('dark-mode');
        localStorage.setItem('darkMode', 'disabled');
        toggleBtn.textContent = '🌙 Modo Escuro';
    }
}

// Ao carregar, verifica a preferência salva
if (localStorage.getItem('darkMode') === 'enabled') {
    setDarkMode(true);
} else {
    setDarkMode(false);
}

// Escuta clique no botão para alternar
toggleBtn.addEventListener('click', () => {
    const isDark = document.body.classList.contains('dark-mode');
    setDarkMode(!isDark);
});

const orikiDiv = document.getElementById('oriki');
const favoritarBtn = document.getElementById('favoritar');
const listaFavoritos = document.getElementById('listaFavoritos');

// Função para carregar favoritos do localStorage
function carregarFavoritos() {
  const favoritos = JSON.parse(localStorage.getItem('favoritosOrikis')) || [];
  listaFavoritos.innerHTML = ''; // limpa a lista

  favoritos.forEach((texto, index) => {
    const li = document.createElement('li');
    li.textContent = texto;

    // Botão para remover favorito
    const btnRemover = document.createElement('button');
    btnRemover.textContent = '❌';
    btnRemover.style.marginLeft = '1rem';
    btnRemover.addEventListener('click', () => {
      removerFavorito(index);
    });

    li.appendChild(btnRemover);
    listaFavoritos.appendChild(li);
  });
}

// Função para adicionar favorito
function adicionarFavorito(texto) {
  if (!texto) return;
  const favoritos = JSON.parse(localStorage.getItem('favoritosOrikis')) || [];
  // Evitar duplicados
  if (favoritos.includes(texto)) {
    alert('Este Oríkì já está nos favoritos!');
    return;
  }
  favoritos.push(texto);
  localStorage.setItem('favoritosOrikis', JSON.stringify(favoritos));
  carregarFavoritos();
}

// Função para remover favorito pelo índice
function removerFavorito(index) {
  const favoritos = JSON.parse(localStorage.getItem('favoritosOrikis')) || [];
  favoritos.splice(index, 1);
  localStorage.setItem('favoritosOrikis', JSON.stringify(favoritos));
  carregarFavoritos();
}

// Evento ao clicar em Favoritar
favoritarBtn.addEventListener('click', () => {
  const textoAtual = orikiDiv.textContent.trim();
  if (textoAtual) {
    adicionarFavorito(textoAtual);
  }
});

// Carregar favoritos ao iniciar a página
carregarFavoritos();